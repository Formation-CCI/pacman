    initGrille(mapEnCours)
    {   
        /* VIDE LA GRILLE */
        grille.innerHTML = " ";
        
        /* CREATION DE LA GRILLE */
        grille.style.display = "grid";
        grille.style.gridTemplateRows = "repeat(22, 40px)";
        grille.style.gridTemplateColumns = "repeat(19, 40px)";
        let i = 0;
        let y = 0;
        
        /* PARCOURE LE TABLEAU ET REMPLIE LA GRILLE */
        while(i < mapEnCours.length)
        {
            y = 0;
            while(y < mapEnCours[i].length)
            {
                if(mapEnCours[i][y] == 0)
                    this.genereMap(grille, "mur", i, y);
                else if(mapEnCours[i][y] == 1)
                    this.genereMap(grille, "sol", i, y)
                else if(mapEnCours[i][y] == 2)
                    this.genereMap(grille, "bonbon", i, y);
                y++;
            }
            i++;
        }
    }
